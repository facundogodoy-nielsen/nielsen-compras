-- 4) Permisos (RLS) — permitir lectura/escritura con la anon key que usan los tableros.
--    Si la tabla ya venía funcionando con los tableros, estas políticas probablemente ya existen.
alter table public.proveedores enable row level security;

do $$
begin
  if not exists (select 1 from pg_policies where tablename='proveedores' and policyname='proveedores_select_anon') then
    create policy proveedores_select_anon on public.proveedores for select using (true);
  end if;
  if not exists (select 1 from pg_policies where tablename='proveedores' and policyname='proveedores_insert_anon') then
    create policy proveedores_insert_anon on public.proveedores for insert with check (true);
  end if;
  if not exists (select 1 from pg_policies where tablename='proveedores' and policyname='proveedores_update_anon') then
    create policy proveedores_update_anon on public.proveedores for update using (true) with check (true);
  end if;
  if not exists (select 1 from pg_policies where tablename='proveedores' and policyname='proveedores_delete_anon') then
    create policy proveedores_delete_anon on public.proveedores for delete using (true);
  end if;
end $$;
