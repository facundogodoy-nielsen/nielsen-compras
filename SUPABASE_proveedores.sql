-- Usada por el Centro de Control (Presupuestos → Base de Datos)
-- y por el Comparativo (Proveedores & PDFs → Base de Datos).
-- Ejecutar en Supabase → SQL Editor. Es idempotente (se puede correr varias veces).
-- ============================================================

-- 1) Crear la tabla si no existe
create table if not exists public.proveedores (
  id              bigint generated by default as identity primary key,
  nombre          text not null,
  cuit            text,
  rubro           text,
  email           text,
  tel             text,        -- TELÉFONO
  direccion       text,
  contacto        text,        -- CONTACTO
  condicion_pago  text,
  tipo            text,
  created_at      timestamptz default now()
);

-- 2) Asegurar TODAS las columnas (por si la tabla ya existía con menos campos)
alter table public.proveedores add column if not exists nombre          text;
alter table public.proveedores add column if not exists cuit            text;  -- CUIT
alter table public.proveedores add column if not exists rubro           text;
alter table public.proveedores add column if not exists email           text;  -- EMAIL
alter table public.proveedores add column if not exists tel             text;  -- TELÉFONO
alter table public.proveedores add column if not exists direccion       text;
alter table public.proveedores add column if not exists contacto        text;  -- CONTACTO
alter table public.proveedores add column if not exists condicion_pago  text;
alter table public.proveedores add column if not exists tipo            text;
alter table public.proveedores add column if not exists created_at      timestamptz default now();

-- 3) (Opcional pero recomendado) evitar proveedores duplicados por nombre.
--    El upsert de ambos tableros usa el NOMBRE como clave, así que conviene un índice único.
--    Si este paso falla por duplicados previos, ejecutá primero el bloque de de-duplicado de abajo.
create unique index if not exists proveedores_nombre_uidx on public.proveedores (nombre);

